package com.big.tuwien.ModelManager.imodel;

public class ModelParsingException extends Exception {
	private static final long serialVersionUID = 9089830578L;

	
	public ModelParsingException() {
		super();
	}

	
	public ModelParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	
	public ModelParsingException(String arg0) {
		super(arg0);
	}

	
	public ModelParsingException(Throwable arg0) {
		super(arg0);
	}

}
