package com.big.tuwien.SmartMatcher.operators.heterogenic.c2r;

public class C2RException extends Exception {

	private static final long serialVersionUID = 8874893257938457L;
	
	public C2RException() {
		super();
	}

	public C2RException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public C2RException(String arg0) {
		super(arg0);
	}

	public C2RException(Throwable arg0) {
		super(arg0);
	}

}
