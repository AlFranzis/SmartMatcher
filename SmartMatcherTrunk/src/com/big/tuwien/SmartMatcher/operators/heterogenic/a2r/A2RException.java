package com.big.tuwien.SmartMatcher.operators.heterogenic.a2r;

public class A2RException extends Exception {

	private static final long serialVersionUID = 8874893257938457L;
	
	public A2RException() {
		super();
	}

	public A2RException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public A2RException(String arg0) {
		super(arg0);
	}

	public A2RException(Throwable arg0) {
		super(arg0);
	}

}
