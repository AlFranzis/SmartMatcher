package com.big.tuwien.SmartMatcher.operators.homogenic.r2r;

public class R2RException extends Exception {

	private static final long serialVersionUID = 64456363257938457L;
	
	public R2RException() {
		super();
	}

	public R2RException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public R2RException(String arg0) {
		super(arg0);
	}

	public R2RException(Throwable arg0) {
		super(arg0);
	}

}
