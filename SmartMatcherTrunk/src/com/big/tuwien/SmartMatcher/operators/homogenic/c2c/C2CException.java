package com.big.tuwien.SmartMatcher.operators.homogenic.c2c;

public class C2CException extends Exception {

	private static final long serialVersionUID = 64456363257938457L;
	
	public C2CException() {
		super();
	}

	public C2CException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public C2CException(String arg0) {
		super(arg0);
	}

	public C2CException(Throwable arg0) {
		super(arg0);
	}

}
