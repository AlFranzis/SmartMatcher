package com.big.tuwien.SmartMatcher.strategy.pso;

public class PSORuntimeException extends RuntimeException {
	private static final long serialVersionUID = 8728723247L;

	
	public PSORuntimeException() {
		super();
	}

	
	public PSORuntimeException(String message, Throwable cause) {
		super(message, cause);
	}

	
	public PSORuntimeException(String message) {
		super(message);
	}

	
	public PSORuntimeException(Throwable cause) {
		super(cause);
	}
}
