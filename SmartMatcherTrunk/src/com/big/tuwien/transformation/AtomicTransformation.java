package com.big.tuwien.transformation;

import java.util.Collection;
import java.util.Map;

import com.big.tuwien.ModelManager.imodel.InstanceElement;
import com.big.tuwien.SmartMatcher.views.iterator.Role;

public class AtomicTransformation {
	private Map<Role,InstanceElement<?,?>> lhsObjects;
	private Map<Role,InstanceElement<?,?>> rhsObjects;
	
	
	public Map<Role, InstanceElement<?, ?>> getLhsObjects() {
		return lhsObjects;
	}
	
	
	public void setLhsObjects(Map<Role, InstanceElement<?, ?>> lhsObjects) {
		this.lhsObjects = lhsObjects;
	}
	
	
	public Map<Role, InstanceElement<?, ?>> getRhsObjects() {
		return rhsObjects;
	}
	
	
	public void setRhsObjects(Map<Role, InstanceElement<?, ?>> rhsObjects) {
		this.rhsObjects = rhsObjects;
	}
	
	
	public Collection<InstanceElement<?,?>> getLHSObjects() {
		return lhsObjects.values();
	}
	
	
	public Collection<InstanceElement<?,?>> getRHSObjects() {
		return rhsObjects.values();
	}
	
	
	
}
