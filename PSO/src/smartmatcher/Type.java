package smartmatcher;

public class Type extends Predicate<Type.TYPE> {
	public final static String NAME = "type";
	public static enum TYPE  {INTEGER, STRING};
	
	
	public Type() {
		super(NAME);
	}

	public Type(TYPE value) {
		super(NAME, value);
	}
	
	
	public String toString() {
		return NAME + ": " + value;
	}
}
