package com.big.tuwien.smartmatcher.strategy.sreuse;

public class ReuseRuntimeException extends RuntimeException {
	private static final long serialVersionUID = 7678730923445L;

	public ReuseRuntimeException() {
		super();
	}

	
	public ReuseRuntimeException(String message, Throwable cause) {
		super(message, cause);
	}

	
	public ReuseRuntimeException(String message) {
		super(message);
	}

	
	public ReuseRuntimeException(Throwable cause) {
		super(cause);
	}
	
	

}
