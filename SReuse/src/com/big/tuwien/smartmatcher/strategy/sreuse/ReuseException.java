package com.big.tuwien.smartmatcher.strategy.sreuse;

public class ReuseException extends Exception {
	private static final long serialVersionUID = 7678730923445L;

	public ReuseException() {
		super();
	}

	
	public ReuseException(String message, Throwable cause) {
		super(message, cause);
	}

	
	public ReuseException(String message) {
		super(message);
	}

	
	public ReuseException(Throwable cause) {
		super(cause);
	}
	
	

}
